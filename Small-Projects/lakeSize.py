sizes = [7340, 2117, 22300, 31700, 1679, 1014, 23000, 9910, 695]

largest = max(sizes)

print("Largest lake in sq miles: ")
print(largest)

smallest = min(sizes)

print("10th largest lake in sq miles: ")
print(smallest)

difference = largest - smallest

print("Difference between lakes in sq miles: ")
print(difference)

